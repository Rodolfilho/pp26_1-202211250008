package padroes.atividadeDebate.configurartempo;

public class ConfigurarTempo {

    private int tempoPergunta;
    private int tempoResposta;
    private int tempoReplica;
    private int tempoTreplica;

    public int getTempoTreplica() {
        return tempoTreplica;
    }

    public void setTempoTreplica(int tempoTreplica) {
        this.tempoTreplica = tempoTreplica;
    }

    public int getTempoResposta() {
        return tempoResposta;
    }

    public void setTempoResposta(int tempoResposta) {
        this.tempoResposta = tempoResposta;
    }

    public int getTempoPergunta() {
        return tempoPergunta;
    }

    public void setTempoPergunta(int tempoPergunta) {
        this.tempoPergunta = tempoPergunta;
    }

    public int getTempoReplica() {
        return tempoReplica;
    }

    public void setTempoReplica(int tempoReplica) {
        this.tempoReplica = tempoReplica;
    }
}
